// testGraph is a small hand-built graph exercising every feature a diagram needs:
// pointer-receiver methods, a value/pointer Score twin (ambiguity), a boundary
// pseudo-node endpoint (drawable as a core node), a DUPLICATE (from,to) pair
// carried twice under different modes (the unique-pair dedup), and a genuine
// self-edge (a self-loop core edge).
func testGraph() *graph.Graph {
	return &graph.Graph{
		Nodes: []graph.Node{
			{FQN: "(*svc/handler.App).Create", Tier: 1},
			{FQN: "(*svc/repo.Store).Save", Tier: 2},
			{FQN: "(*svc/repo.Store).Load", Tier: 2},
			{FQN: "svc/scoring.Score", Tier: 2},
			{FQN: "(*svc/scoring.Remote).Score", Tier: 3},
			{FQN: "(*svc/worker.Loop).Run", Tier: 1},
		},
		Edges: []graph.Edge{
			{From: "(*svc/handler.App).Create", To: "(*svc/repo.Store).Save", Tier: 2},
			// Same pair, concurrent mode: a distinct RECORD, one unique PAIR.
			{From: "(*svc/handler.App).Create", To: "(*svc/repo.Store).Save", Tier: 2, Concurrent: true},
			{From: "(*svc/handler.App).Create", To: "boundary:db QueryContext", Tier: 1, Boundary: "db"},
			{From: "(*svc/repo.Store).Save", To: "boundary:db INSERT rows", Tier: 1, Boundary: "outbound-sync"},
			{From: "(*svc/worker.Loop).Run", To: "(*svc/worker.Loop).Run", Tier: 1}, // self-loop
		},
	}
}

func oneGraph(g *graph.Graph) map[string]*graph.Graph { return map[string]*graph.Graph{"g": g} }

func gen(t *testing.T, m *Manifest, graphs map[string]*graph.Graph) string {
	t.Helper()
	out, err := Generate(m, graphs)
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}
	return out
}

func node(id, fn, label string) ManifestNode {
	return ManifestNode{ID: id, Graph: "g", Fn: fn, Label: label}
}

// TestCoreEdgesInduced pins the induced-subgraph rule: a present pair is drawn, an
// absent pair is not, a self-edge is drawn as a self-loop, and NOTHING but a graph
// edge produces a solid arrow. It is a byte-exact pin over a subgraph-free,
// overlay-free manifest so both the edge SET and the emission ORDER are enforced.
func TestCoreEdgesInduced(t *testing.T) {
	m := &Manifest{
		ID: "CORE-TEST", Direction: "LR",
		Nodes: []ManifestNode{
			node("A", "handler.App).Create", "Create"),
			node("B", "repo.Store).Save", "Save"),
			node("C", "repo.Store).Load", "Load"),
			node("DB", "boundary:db QueryContext", "db QueryContext"),
			node("LP", "worker.Loop).Run", "Loop.Run"),
		},
	}
	want := "flowchart LR\n" +
		"    %% generated by gen-diagram from CORE-TEST/diagram.json @ unstamped — do not hand-edit the core\n" +
		"    A[\"Create\"] --> B[\"Save\"]\n" + // present pair drawn
		"    A --> DB[\"db QueryContext\"]\n" + // boundary pseudo-node as a core node
		"    LP[\"Loop.Run\"] --> LP\n" + // self-loop drawn
		"    C[\"Load\"]\n" // no core edge touches C → standalone (absent pairs not drawn)
	if got := gen(t, m, oneGraph(testGraph())); got != want {
		t.Errorf("core induction:\n got:\n%s\nwant:\n%s", got, want)
	}
}

// TestCrossGraphNeverDrawn pins that an edge existing in BOTH graphs is still not
// drawn between two nodes bound to DIFFERENT logical graphs — a diagram edge is a
// single-graph fact.
func TestCrossGraphNeverDrawn(t *testing.T) {
	g1 := &graph.Graph{
		Nodes: []graph.Node{{FQN: "svc/a.X", Tier: 1}, {FQN: "svc/a.Y", Tier: 1}},
		Edges: []graph.Edge{{From: "svc/a.X", To: "svc/a.Y", Tier: 1}},
	}
	g2 := &graph.Graph{
		Nodes: []graph.Node{{FQN: "svc/a.X", Tier: 1}, {FQN: "svc/a.Y", Tier: 1}},
		Edges: []graph.Edge{{From: "svc/a.X", To: "svc/a.Y", Tier: 1}},
	}
	m := &Manifest{
		ID: "X", Direction: "LR",
		Nodes: []ManifestNode{
			{ID: "NA", Graph: "one", Fn: "a.X", Label: "X"},
			{ID: "NB", Graph: "two", Fn: "a.Y", Label: "Y"},
		},
	}
	out := gen(t, m, map[string]*graph.Graph{"one": g1, "two": g2})
	if strings.Contains(out, "-->") {
		t.Errorf("cross-graph pair must not draw a solid edge:\n%s", out)
	}
}

// TestTiersAnnotation pins the ` (tN)` suffix read from the graph, including the
// boundary-endpoint-no-tier honesty case: a resolved boundary pseudo-node has no
// tier, so it gets no suffix (never a fabricated one).
func TestTiersAnnotation(t *testing.T) {
	m := &Manifest{
		ID: "T", Direction: "LR", Tiers: true,
		Nodes: []ManifestNode{
			node("A", "handler.App).Create", "Create"),                // tier 1
			node("S", "scoring.Remote).Score", "Score"),               // tier 3
			node("DB", "boundary:db QueryContext", "db QueryContext"), // no tier
		},
	}
	out := gen(t, m, oneGraph(testGraph()))
	for _, want := range []string{`A["Create (t1)"]`, `S["Score (t3)"]`, `DB["db QueryContext"]`} {
		if !strings.Contains(out, want) {
			t.Errorf("tiers output missing %q:\n%s", want, out)
		}
	}
	// The boundary node must carry NO tier suffix (honest omission).
	if strings.Contains(out, "db QueryContext (t") {
		t.Errorf("boundary pseudo-node must not get a tier suffix:\n%s", out)
	}
}

