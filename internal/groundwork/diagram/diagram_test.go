func reversed(g *graph.Graph) *graph.Graph {
	c := *g
	c.Nodes = append([]graph.Node(nil), g.Nodes...)
	c.Edges = append([]graph.Edge(nil), g.Edges...)
	for i, j := 0, len(c.Nodes)-1; i < j; i, j = i+1, j-1 {
		c.Nodes[i], c.Nodes[j] = c.Nodes[j], c.Nodes[i]
	}
	for i, j := 0, len(c.Edges)-1; i < j; i, j = i+1, j-1 {
		c.Edges[i], c.Edges[j] = c.Edges[j], c.Edges[i]
	}
	return &c
}

