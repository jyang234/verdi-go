// TestIDValidation pins the mermaid-identifier charset, the single id namespace
// (node/overlay/subgraph collide), a dangling subgraph member, a dangling overlay
// endpoint, and a node placed in two subgraphs.
func TestIDValidation(t *testing.T) {
	base := func() *Manifest {
		return &Manifest{ID: "X", Direction: "LR", Nodes: []ManifestNode{node("A", "handler.App).Create", "Create")}}
	}
	// Bad charset on a node id.
	m := base()
	m.Nodes[0].ID = "bad-id"
	if _, err := Generate(m, oneGraph(testGraph())); err == nil || !strings.Contains(err.Error(), "valid mermaid identifier") {
		t.Errorf("bad id charset = %v, want an identifier error", err)
	}
	// Duplicate id across node + overlay node.
	m = base()
	m.Overlays = Overlays{Nodes: []OverlayNode{{ID: "A", Label: "dup"}}}
	if _, err := Generate(m, oneGraph(testGraph())); err == nil || !strings.Contains(err.Error(), "duplicate id") {
		t.Errorf("duplicate id = %v, want a duplicate-id error", err)
	}
	// Subgraph member that is not a manifest node.
	m = base()
	m.Subgraphs = []Subgraph{{ID: "S", Nodes: []string{"NOPE"}}}
	if _, err := Generate(m, oneGraph(testGraph())); err == nil || !strings.Contains(err.Error(), "not a manifest node id") {
		t.Errorf("dangling subgraph member = %v, want a dangling-member error", err)
	}
	// Overlay edge endpoint that references nothing.
	m = base()
	m.Overlays = Overlays{Edges: []OverlayEdge{{From: "A", To: "NOPE"}}}
	if _, err := Generate(m, oneGraph(testGraph())); err == nil || !strings.Contains(err.Error(), "not a manifest node or overlay node id") {
		t.Errorf("dangling overlay endpoint = %v, want an unknown-endpoint error", err)
	}
	// A SUBGRAPH id is a registered id but has no node declaration, so it cannot be
	// an overlay edge endpoint (else it would emit a broken empty endpoint).
	m = base()
	m.Subgraphs = []Subgraph{{ID: "S", Nodes: []string{"A"}}}
	m.Overlays = Overlays{Nodes: []OverlayNode{{ID: "OV", Label: "x"}}, Edges: []OverlayEdge{{From: "S", To: "OV"}}}
	if _, err := Generate(m, oneGraph(testGraph())); err == nil || !strings.Contains(err.Error(), "not a manifest node or overlay node id") {
		t.Errorf("subgraph-id overlay endpoint = %v, want a rejection", err)
	}
	// A node in two subgraphs.
	m = base()
	m.Subgraphs = []Subgraph{{ID: "S1", Nodes: []string{"A"}}, {ID: "S2", Nodes: []string{"A"}}}
	if _, err := Generate(m, oneGraph(testGraph())); err == nil || !strings.Contains(err.Error(), "two subgraphs") {
		t.Errorf("node in two subgraphs = %v, want a two-subgraphs error", err)
	}
}

