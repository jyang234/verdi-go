// TestLoadFileStrict pins the fail-closed decode: an unknown key and trailing data
// both error, while a well-formed manifest loads (parity with graph.Load).
func TestLoadFileStrict(t *testing.T) {
	write := func(s string) string {
		p := filepath.Join(t.TempDir(), "m.json")
		if err := os.WriteFile(p, []byte(s), 0o644); err != nil {
			t.Fatal(err)
		}
		return p
	}
	if _, err := LoadFile(write(`{"id":"X","direction":"LR","bogus":1,"nodes":[]}`)); err == nil {
		t.Error("unknown field must fail strict decode")
	}
	if _, err := LoadFile(write(`{"id":"X","direction":"LR","nodes":[]} trailing`)); err == nil {
		t.Error("trailing data after manifest JSON must error")
	}
	m, err := LoadFile(write(`{"id":"X","direction":"LR","nodes":[{"id":"A","graph":"g","fn":"f","label":"L"}]}`))
	if err != nil || m.ID != "X" || len(m.Nodes) != 1 {
		t.Errorf("valid manifest: m=%+v err=%v", m, err)
	}
}
